package com.ongc.liferay.passion.model;

public class PassionTags {
	
	private String subPassionId;
	private String tags;
	private int tagsNo;
	
	public String getSubPassionId() {
		return subPassionId;
	}
	public void setSubPassionId(String subPassionId) {
		this.subPassionId = subPassionId;
	}
	public String getTags() {
		return tags;
	}
	public void setTags(String tags) {
		this.tags = tags;
	}
	public int getTagsNo() {
		return tagsNo;
	}
	public void setTagsNo(int tagsNo) {
		this.tagsNo = tagsNo;
	}

}
