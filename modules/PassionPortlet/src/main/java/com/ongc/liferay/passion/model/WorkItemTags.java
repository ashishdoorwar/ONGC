package com.ongc.liferay.passion.model;

public class WorkItemTags {
	
	private String empCpf;
	private String tags;
	private int tagsNo;
	private String url;
	public String getEmpCpf() {
		return empCpf;
	}
	public void setEmpCpf(String empCpf) {
		this.empCpf = empCpf;
	}
	public String getTags() {
		return tags;
	}
	public void setTags(String tags) {
		this.tags = tags;
	}
	public int getTagsNo() {
		return tagsNo;
	}
	public void setTagsNo(int tagsNo) {
		this.tagsNo = tagsNo;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	

}
